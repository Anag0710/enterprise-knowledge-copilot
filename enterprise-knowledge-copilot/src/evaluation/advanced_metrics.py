"""
Advanced evaluation metrics for answer quality assessment.
"""
from typing import List, Dict
from dataclasses import dataclass

try:
    from rouge_score import rouge_scorer
    ROUGE_AVAILABLE = True
except ImportError:
    ROUGE_AVAILABLE = False

try:
    import bert_score
    BERT_SCORE_AVAILABLE = True
except ImportError:
    BERT_SCORE_AVAILABLE = False


@dataclass
class AdvancedMetrics:
    """Container for advanced evaluation metrics."""
    rouge1_f1: float
    rouge2_f1: float
    rougeL_f1: float
    bert_score_f1: float
    avg_score: float


class AdvancedEvaluator:
    """
    Evaluate answer quality using advanced NLP metrics.
    
    Metrics:
    - ROUGE: Measures n-gram overlap (good for summarization quality)
    - BERTScore: Measures semantic similarity using BERT embeddings
    """
    
    def __init__(self):
        self.rouge_available = ROUGE_AVAILABLE
        self.bert_available = BERT_SCORE_AVAILABLE
        
        if self.rouge_available:
            self.rouge_scorer = rouge_scorer.RougeScorer(
                ['rouge1', 'rouge2', 'rougeL'],
                use_stemmer=True
            )
        else:
            print("Warning: rouge-score not available. Run: pip install rouge-score")
        
        if not self.bert_available:
            print("Warning: bert-score not available. Run: pip install bert-score")
    
    def evaluate(
        self,
        generated_answer: str,
        reference_answer: str
    ) -> AdvancedMetrics:
        """
        Evaluate generated answer against reference.
        
        Args:
            generated_answer: Answer generated by the agent
            reference_answer: Ground truth / expected answer
            
        Returns:
            AdvancedMetrics with all scores
        """
        rouge1 = rouge2 = rougeL = 0.0
        bert_f1 = 0.0
        
        # Calculate ROUGE scores
        if self.rouge_available and generated_answer and reference_answer:
            try:
                scores = self.rouge_scorer.score(reference_answer, generated_answer)
                rouge1 = scores['rouge1'].fmeasure
                rouge2 = scores['rouge2'].fmeasure
                rougeL = scores['rougeL'].fmeasure
            except Exception as e:
                print(f"Warning: ROUGE calculation failed: {e}")
        
        # Calculate BERTScore
        if self.bert_available and generated_answer and reference_answer:
            try:
                P, R, F1 = bert_score.score(
                    [generated_answer],
                    [reference_answer],
                    lang='en',
                    verbose=False
                )
                bert_f1 = F1.item()
            except Exception as e:
                print(f"Warning: BERTScore calculation failed: {e}")
        
        # Calculate average
        scores = [rouge1, rouge2, rougeL, bert_f1]
        avg = sum(scores) / len(scores) if scores else 0.0
        
        return AdvancedMetrics(
            rouge1_f1=rouge1,
            rouge2_f1=rouge2,
            rougeL_f1=rougeL,
            bert_score_f1=bert_f1,
            avg_score=avg
        )
    
    def evaluate_batch(
        self,
        generated_answers: List[str],
        reference_answers: List[str]
    ) -> Dict[str, float]:
        """
        Evaluate multiple answer pairs and return aggregate metrics.
        
        Args:
            generated_answers: List of generated answers
            reference_answers: List of reference answers
            
        Returns:
            Dictionary with averaged metrics
        """
        if len(generated_answers) != len(reference_answers):
            raise ValueError("Length mismatch between generated and reference answers")
        
        all_metrics = []
        
        for gen, ref in zip(generated_answers, reference_answers):
            metrics = self.evaluate(gen, ref)
            all_metrics.append(metrics)
        
        # Calculate averages
        if not all_metrics:
            return {
                "avg_rouge1": 0.0,
                "avg_rouge2": 0.0,
                "avg_rougeL": 0.0,
                "avg_bert_score": 0.0,
                "overall_avg": 0.0
            }
        
        return {
            "avg_rouge1": sum(m.rouge1_f1 for m in all_metrics) / len(all_metrics),
            "avg_rouge2": sum(m.rouge2_f1 for m in all_metrics) / len(all_metrics),
            "avg_rougeL": sum(m.rougeL_f1 for m in all_metrics) / len(all_metrics),
            "avg_bert_score": sum(m.bert_score_f1 for m in all_metrics) / len(all_metrics),
            "overall_avg": sum(m.avg_score for m in all_metrics) / len(all_metrics),
            "count": len(all_metrics)
        }
